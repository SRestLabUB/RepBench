#!/usr/bin/env python3

import json
import os
import shutil
import subprocess
import argparse
import re

parser = argparse.ArgumentParser(description='Generate ePDGs for testcases')

parser.add_argument('-i', '--input', required=True, help='Testcase input directory')
parser.add_argument('-t', '--targets', required=True, help='Path to file that holds the build targets')
args = parser.parse_args()

targets_path = args.targets
build_targets = {}
with open(f'{targets_path}', 'r') as f:
    build_targets = json.load(f)

for root, dirs, files in os.walk(args.input):
    if 'cse713' in root.split("/")[-1]:
        # Read metadata file to grab CWE
        metadata = {}
        with open(root + '/metadata.json', 'r') as f:
            metadata = json.load(f)

        cwe_num = metadata['cwe'].split("-")[-1]
        filename = metadata['project_file_path'].split("/")[-1]
        build_target = build_targets[metadata['project_name']]
        # Run `hector` to build ePDG with /project_src
        proj_path = '/home/ubuntu/' + root + '/project_src'
        subprocess.run(f"docker exec epdg_container bash -c '\
                cd {proj_path} \
                && hector configure --llap-lib-dir $HECTOR_LIB cmake \"{build_target}\" {cwe_num} \
                && cd {proj_path}/hector_build \
                && ninja -f hector.ninja'", shell=True)

        os.makedirs(root + '/base_epdg', exist_ok=True)
        os.makedirs(root + '/split_epdg', exist_ok=True)
        subprocess.run(f"docker cp epdg_container:{proj_path}/hector_build/hector-{cwe_num}.json {root}/base_epdg", shell=True)
        # Decompose ePDG into nodes and links and strip out the columns we only care about
        print("Decomposing ePDG...")
        epdg_file = f"{root}/base_epdg/hector-{cwe_num}.json"
        subprocess.run(f"jq -c '[.links[] | {{source_id: .source, target_id: .target, link_type: .type, link_dtype: .dtype}}]' {epdg_file} > {root}/base_epdg/links.json", shell=True)
        subprocess.run(f"jq -c '[.nodes[] | select(.filename != \"\") | {{static_value, operation, node_dtype: .dtype, condition, tag, filename, line_number, id}}]' {epdg_file} > {root}/base_epdg/nodes.json", shell=True)
        line_start = metadata['function_start']
        line_end = metadata['function_end']
        print("Creating ePDG for whole file...")
        subprocess.run(f"./bin/decompose_epdg.py -n {root}/base_epdg/nodes.json -l {root}/base_epdg/links.json -o {root}/split_epdg", shell=True)
        print("Creating ePDG for the testcase function...")
        subprocess.run(f"./bin/decompose_epdg.py -n {root}/base_epdg/nodes.json -l {root}/base_epdg/links.json -o {root}/split_epdg -s {line_start} -e {line_end}", shell=True)
        # Keep only ePDG that matches function within `files`
        with os.scandir(f"{root}/split_epdg") as epdgs:
            for epdg in epdgs:
                if epdg.is_file():
                    epdg_comparision_name = epdg.name
                    suffix = "_function_only"
                    if epdg_comparision_name.endswith(suffix):
                        epdg_comparision_name = epdg_comparision_name[:-len(suffix)]
                    if epdg_comparision_name in filename:
                        matched_epdg = f"{root}/split_epdg/{epdg.name}"
                        subprocess.run(f" rsync -a {matched_epdg} {root}", shell=True)
                        print(f"Copied matched ePDG file split_epdg/{epdg.name}")

        #Clean up (have docker do it because files generated by ePDG are `root`)
        print("Cleaning up files...")
        subprocess.run(f"docker exec epdg_container bash -c 'rm -rf {root}/project_src/hector_build'", shell=True)
        subprocess.run(f"docker exec epdg_container bash -c 'rm -rf {root}/project_src/../base_epdg'", shell=True)
        subprocess.run(f"docker exec epdg_container bash -c 'rm -rf {root}/project_src/../split_epdg'", shell=True)
        print(f"Test case ePDG generation complete.")
